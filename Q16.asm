; 第16题：键盘输入十个数，分别统计0到9，10到99，100以上的数的个数，并显示结果。
; Coded by: Mosles Signall
; Date: 2025-Oct-30

DATAS SEGMENT
    NUMBER DW 0
    ONE DB 0
    DUICE DB 0
    TRI DB 0
    TEN DB "10",13,10,'$'
DATAS ENDS

STACKS SEGMENT
    DW 100 DUP(?)
STACKS ENDS

CODES SEGMENT
    ASSUME CS:CODES,DS:DATAS,SS:STACKS
START:
    MOV AX,DATAS
    MOV DS,AX
    MOV CX,10
    MOV BX,10

INPUT:
    MOV NUMBER,0

INPUT_LOOP:
    MOV AH,1
    INT 21H
    CMP AL,0DH
    JE JUDGE
    SUB AL,30H
    PUSH AX
    MOV AX,NUMBER
    MUL BX
    MOV NUMBER,AX
    POP AX
    MOV AH,0
    ADD NUMBER,AX
    JMP INPUT_LOOP

JUDGE:
    MOV DX,NUMBER
    CMP DX,10
    JB ONLY_ONE
    CMP DX,100
    JB ONLY_DUICE
    JMP ONLY_TRI

DISPLAY:
    MOV DL,ONE
    CALL SHOW
    MOV DL,DUICE
    CALL SHOW
    MOV DL,TRI
    CALL SHOW

EXIT:
    MOV AH,4CH
    INT 21H

ONLY_ONE:
    INC ONE
    LOOP INPUT
    JMP DISPLAY

ONLY_DUICE:
    INC DUICE
    LOOP INPUT
    JMP DISPLAY

ONLY_TRI:
    INC TRI
    LOOP INPUT
    JMP DISPLAY

TENS:
    MOV AH,9
    MOV DX,OFFSET TEN
    INT 21H
    RET

NORM:
    ADD DL,30H
    MOV AH,2
    INT 21H
    MOV DL,0AH
    INT 21H
    RET

SHOW:
    CMP DL,10
    JNE NORMAL
    CALL TENS
    NORMAL:
    CALL NORM
    RET
CODES ENDS
    END START