DATAS SEGMENT
    ADDITION DW 0
DATAS ENDS

STACKS SEGMENT
    DW 100 DUP(?)
STACKS ENDS

CODES SEGMENT
    ASSUME CS:CODES,DS:DATAS,SS:STACKS
START:
    MOV AX,DATAS
    MOV DS,AX

    MOV BX,2
INPUT:
    MOV AH,1
    INT 21H
    MOV AH,0
    CMP AL,0DH
    JE INPUT_END
    SUB AL,30H
    PUSH AX
    POP CX
    MOV AX,ADDITION
    MUL BX
    MOV ADDITION,AX
    ADD ADDITION,CX
    JMP INPUT

INPUT_END:
    MOV BX,10
    MOV CX,0
    MOV AX,ADDITION

DIVIDE:
    MOV DX,0
    DIV BX
    ADD DX,30H
    CMP DX,39H
    JA ADD07
    ADD07_END:
    PUSH DX
    INC CX
    CMP AX,0
    JE DISPLAY
    JMP DIVIDE

DISPLAY:
    MOV AH,2
DISPLAY_LOOP:
    POP DX
    INT 21H
    LOOP DISPLAY_LOOP

EXIT:
    MOV AH,4CH
    INT 21H

ADD07:
    ADD DX,07H
    JMP ADD07_END
CODES ENDS
    END START