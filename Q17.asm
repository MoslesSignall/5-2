DATAS SEGMENT
    MAX DW 0
    MIN DW 65535
    NUMBER DW 0
DATAS ENDS

STACKS SEGMENT
    DW 100 DUP(?)
STACKS ENDS

CODES SEGMENT
    ASSUME CS:CODES, DS:DATAS, SS:STACKS

START:
    MOV AX, DATAS
    MOV DS, AX
    MOV CX, 3
    MOV BX, 10

INPUT:
    MOV AH,1
    INT 21H
    CMP AL,0DH
    JE JUDGE
    SUB AL,30H
    PUSH AX
    MOV AX,NUMBER
    MUL BX
    MOV NUMBER,AX
    POP AX
    MOV AH,0
    ADD NUMBER,AX
    JMP INPUT

JUDGE:
    MOV DX,NUMBER
    CMP DX,MAX
    JA CHANGE_MAX
    CMP DX,MIN
    JB CHANGE_MIN
    MOV NUMBER,0
    LOOP INPUT

DISPLAY:
    MOV AX,MAX
    CALL PRINT_NUMBER
    CALL NEWLINE
    MOV AX,MIN
    CALL PRINT_NUMBER
    CALL NEWLINE

EXIT:
    MOV AH,4CH
    INT 21H

CHANGE_MAX:
    MOV MAX,DX
    JMP JUDGE

CHANGE_MIN:
    MOV MIN,DX
    JMP JUDGE

PRINT_NUMBER PROC
    MOV BX,10
    MOV CX,0
    
DIV_LOOP:
    MOV DX,0
    DIV BX
    ADD DL,30H
    PUSH DX
    INC CX
    CMP AX,0
    JNE DIV_LOOP
    
OUTPUT_LOOP:
    POP DX
    MOV AH,2
    INT 21H
    LOOP OUTPUT_LOOP
    RET
PRINT_NUMBER ENDP

NEWLINE PROC
    MOV DL,0AH
    MOV AH,2
    INT 21H
    RET
NEWLINE ENDP

CODES ENDS
    END START